# Matrizes

# Criando uma lista de listas (matriz)
matriz = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Acessando o primeiro elemento da primeira lista
print(matriz[0][0])  # Saída: 1

# Acessando o segundo elemento da primeira lista
print(matriz[0][1])  # Saída: 2

# Acessando o terceiro elemento da primeira lista
print(matriz[0][2])  # Saída: 3

# Acessando o primeiro elemento da segunda lista
print(matriz[1][0])  # Saída: 4

# Acessando o segundo elemento da segunda lista
print(matriz[1][1])  # Saída: 5

# Acessando o terceiro elemento da segunda lista
print(matriz[1][2])  # Saída: 6

# Acessando o primeiro elemento da terceira lista
print(matriz[2][0])  # Saída: 7

# Acessando o segundo elemento da terceira lista
print(matriz[2][1])  # Saída: 8

# Acessando o terceiro elemento da terceira lista
print(matriz[2][2])  # Saída: 9

# Acessando o último elemento da primeira lista
print(matriz[0][-1])  # Saída: 3

# Acessando o penúltimo elemento da primeira lista
print(matriz[0][-2])  # Saída: 2

# Acessando o antepenúltimo elemento da primeira lista
print(matriz[0][-3])  # Saída: 1

# Acessando o último elemento da segunda lista
print(matriz[1][-1])  # Saída: 6

# Acessando o penúltimo elemento da segunda lista
print(matriz[1][-2])  # Saída: 5

# Acessando o antepenúltimo elemento da segunda lista
print(matriz[1][-3])  # Saída: 4

# Acessando o último elemento da terceira lista
print(matriz[2][-1])  # Saída: 9

# Acessando o penúltimo elemento da terceira lista
print(matriz[2][-2])  # Saída: 8

# Acessando o antepenúltimo elemento da terceira lista
print(matriz[2][-3])  # Saída: 7

# Acessando o último elemento da lista
print(matriz[-1][-1])  # Saída: 9

# Acessando todas as lista
print(matriz[::])  # Saída: [7, 8, 9]

