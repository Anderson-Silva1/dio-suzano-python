lista = ["maçã", "banana", "laranja", "uva", "abacaxi"]

# Acessando o primeiro elemento da lista
print(lista[0])  # Saída: maçã

# Acessando o segundo elemento da lista
print(lista[1])  # Saída: banana

# Acessando o terceiro elemento da lista
print(lista[2])  # Saída: laranja

# Acessando o quarto elemento da lista
print(lista[3])  # Saída: uva

# Acessando o quinto elemento da lista
print(lista[4])  # Saída: abacaxi

#--------------------#

# Acessando o último elemento da lista
print(lista[-1])  # Saída: abacaxi

# Acessando o penúltimo elemento da lista
print(lista[-2])  # Saída: uva

# Acessando o antepenúltimo elemento da lista
print(lista[-3])  # Saída: laranja

# Acessando o antepenúltimo elemento da lista
print(lista[-4])  # Saída: banana

# Acessando o antepenúltimo elemento da lista
print(lista[-5])  # Saída: maçã

