# Tipos de dados em Python

print(10)  # Inteiro
print(type(10))  # Tipo do dado: <class 'int'>

print(10.5)  # Float
print(type(10.5))  # Tipo do dado: <class 'float'>

print("10")  # String
print(type("10"))  # Tipo do dado: <class 'str'>

print(True)  # Booleano
print(type(True))  # Tipo do dado: <class 'bool'>

print(False)  # Booleano
print(type(False))  # Tipo do dado: <class 'bool'>

print([1, 2, 3])  # Lista
print(type([1, 2, 3]))  # Tipo do dado: <class 'list'>

print((1, 2, 3))  # Tupla
print(type((1, 2, 3)))  # Tipo do dado: <class 'tuple'>

print({1, 2, 3})  # Conjunto
print(type({1, 2, 3}))  # Tipo do dado: <class 'set'>

print({"a": 1, "b": 2})  # Dicion√°rio
print(type({"a": 1, "b": 2}))  # Tipo do dado: <class 'dict'>


